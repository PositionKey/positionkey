function pkSetLang(lang) {
  var t = PK_TRANSLATIONS[lang];
  var m = PK_LANG_META[lang];
  if (!t || !m) return;

  // Salva preferenza
  try { localStorage.setItem('pk_lang', lang); } catch(e) {}

  // Direzione testo (per arabo)
  document.documentElement.setAttribute('dir', m.dir);
  document.documentElement.setAttribute('lang', lang);

  // Aggiorna tutti gli elementi con data-i18n
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.getAttribute('data-i18n-html')) {
        el.innerHTML = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });

  // Aggiorna il selettore lingua attivo
  document.querySelectorAll('.pk-lang-item').forEach(function(item) {
    item.classList.toggle('active', item.getAttribute('data-lang') === lang);
  });

  var current = document.getElementById('pkLangCurrent');
  if (current) current.textContent = m.flag + ' ' + lang.toUpperCase();
}

function pkInitLang() {
  var saved = '';
  try { saved = localStorage.getItem('pk_lang') || ''; } catch(e) {}
  var browserLang = (navigator.language || 'it').slice(0, 2).toLowerCase();
  var lang = saved || (PK_TRANSLATIONS[browserLang] ? browserLang : 'it');
  pkSetLang(lang);
}
